  //設定步驟樣式
  const stepClass = (index) => {
    if (index === finishTargetStep.value) {
      return 'ingStep animate__animated animate__flipInX';
    } else if (index < finishTargetStep.value) {
      return 'finishStep';// animate__animated animate__hinge
    } else {
      return '';
    }
  };
  //上一步
  const clickPrevious = () => {
    if (finishTargetStep.value > 0) {
      finishTargetStep.value--;
    }
  };
  //下一步
  const clickNext = () => {
    if (finishTargetStep.value < steps.length - 1) {
      finishTargetStep.value++;
    }
  };
  //直接回推到指定步驟
  const goToStep = (index) => {
    if(index<finishTargetStep.value){
      finishTargetStep.value = index;
    }
  };
  const finish = () => {
    finishTargetStep.value++;
    alert('完成');
  };
